#include "../libft.h"

int	ft_lstsize(t_list *lst)
{
	int	size;

	size = 0;
	if (lst)
	{
		while (lst->next)
		{
			lst = lst->next;
			size++;
		}
		return (size + 1);
	}
	return (0);
}
